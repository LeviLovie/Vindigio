package Java_data_classes;

public class DataPlayer {
    public int x;
    public int y;
    public String quest;
    public int quest_num;
    public DataPlayer(int x, int y, String quest, int quest_num) {
        this.x = x;
        this.y = y;
        this.quest = quest;
        this.quest_num = quest_num;
    }
}
